void main() {
  // OPERADORES DE ATRIBUIÇÃO
  int a = 2;
  a++; // a + 1
  a--; // a - 1
  a += 3; // acrescentamos 3 ao valor de a
  a -= 3;
  a *= 3;
  a %= 2;
  print(a);

  // OPERADORES RELACIONAIS
  // VERDADEIRO OU FALSO
  print(3 > 2); // 3 é maior que 2? verdadeiro ou falso
  print(3 >= 3); // 3 é maior ou igual a 3? verdadeiro ou falso
  print(3 < 4); // 3 é menor que 4?
  print(3 == 3); // 3 é igual a 3?
}
